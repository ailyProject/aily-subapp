const ALIASES = {
  'up-right-from-square': 'arrow-up-right-from-square',
};

function parseArgs(argv) {
  const options = {};
  for (const arg of argv) {
    if (!arg.startsWith('--')) continue;
    const eqIndex = arg.indexOf('=');
    const key = eqIndex >= 0 ? arg.slice(2, eqIndex) : arg.slice(2);
    options[key] = eqIndex >= 0 ? arg.slice(eqIndex + 1) : true;
  }
  return options;
}

function extractIconRule(source, iconName) {
  const resolved = ALIASES[iconName] || iconName;
  const pattern = new RegExp(`\\.fa-${resolved}::before\\s*\\{[^}]*\\}`, 'g');
  const match = source.match(pattern);
  if (!match) {
    throw new Error(`Missing Font Awesome rule for fa-${resolved}`);
  }
  if (iconName === resolved) {
    return match[0];
  }
  return match[0].replace(`.fa-${resolved}::before`, `.fa-${iconName}::before`);
}

